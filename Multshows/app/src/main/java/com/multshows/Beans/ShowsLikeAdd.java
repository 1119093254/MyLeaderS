package com.multshows.Beans;

import java.io.Serializable;

/**
 * 添加点赞
 */
public class ShowsLikeAdd implements Serializable{
    String UserId;//加密收藏者userId
    String AuthorId;//加密作者id
    String ShowsId;//加密作品id
    int Type;//点赞类型 1 作品点赞 2照片点赞

    public ShowsLikeAdd() {
    }

    public String getUserId() {
        return UserId;
    }

    public void setUserId(String userId) {
        UserId = userId;
    }

    public String getAuthorId() {
        return AuthorId;
    }

    public void setAuthorId(String authorId) {
        AuthorId = authorId;
    }

    public String getShowsId() {
        return ShowsId;
    }

    public void setShowsId(String showsId) {
        ShowsId = showsId;
    }

    public int getType() {
        return Type;
    }

    public void setType(int type) {
        Type = type;
    }
}
