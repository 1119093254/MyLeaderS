package com.multshows.Beans;

import java.io.Serializable;
import java.util.List;

/**
 * 描述：动态详情图文报名用户bean
 * 作者：李萧萧
 * 时间：2016.10.9
 */
public class ActivityDetailsUser_Beans implements Serializable{
    String id;//活动发布者id
    String mUserImage;//活动详请用户头像
    String mUserName;//活动详情用户姓名
    List<String> mBusinessCard;//用户名片
    String mPhone;//用户手机号

    public ActivityDetailsUser_Beans(String id,String userImage, String userName, List<String> businessCard, String phone) {
        mUserImage = userImage;
        mUserName = userName;
        mBusinessCard = businessCard;
        mPhone = phone;
        this.id=id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserImage() {
        return mUserImage;
    }

    public void setUserImage(String userImage) {
        mUserImage = userImage;
    }

    public String getUserName() {
        return mUserName;
    }

    public void setUserName(String userName) {
        mUserName = userName;
    }

    public List<String> getBusinessCard() {
        return mBusinessCard;
    }

    public void setBusinessCard(List<String> businessCard) {
        mBusinessCard = businessCard;
    }

    public String getPhone() {
        return mPhone;
    }

    public void setPhone(String phone) {
        mPhone = phone;
    }
}
