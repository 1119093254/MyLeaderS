package com.multshows.Beans;

import java.io.Serializable;

/**
 * 用户收货地址实体类
 */
public class UserAddress implements Serializable{
    private String Id;//加密Id
    private String UserId;//加密UserId
    private String ReceiverName;//收件人姓名
    private String Mobile;//手机号
    private String ProvinceId;//省份id
    private String ProvinceName;//省名
    private String CityId;//市区id
    private String CityName;//市名
    private String DistrictId;//区县id
    private String DistrictName;//区县名
    private String DetailAddress;//详细地址
    private String IsDefault;//是否默认 1默认地址 0非默认地址
    private String ByName;//别名

    public UserAddress() {
    }

    public String getProvinceName() {
        return ProvinceName;
    }

    public void setProvinceName(String provinceName) {
        ProvinceName = provinceName;
    }

    public String getCityname() {
        return CityName;
    }

    public void setCityname(String cityname) {
        CityName = cityname;
    }

    public String getDistrictName() {
        return DistrictName;
    }

    public void setDistrictName(String districtName) {
        DistrictName = districtName;
    }

    public String getByName() {
        return ByName;
    }

    public void setByName(String byName) {
        ByName = byName;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getUserId() {
        return UserId;
    }

    public void setUserId(String userId) {
        UserId = userId;
    }

    public String getReceiverName() {
        return ReceiverName;
    }

    public void setReceiverName(String receiverName) {
        ReceiverName = receiverName;
    }

    public String getMobile() {
        return Mobile;
    }

    public void setMobile(String mobile) {
        Mobile = mobile;
    }

    public String getProvinceId() {
        return ProvinceId;
    }

    public void setProvinceId(String provinceId) {
        ProvinceId = provinceId;
    }

    public String getCityId() {
        return CityId;
    }

    public void setCityId(String cityId) {
        CityId = cityId;
    }

    public String getDistrictId() {
        return DistrictId;
    }

    public void setDistrictId(String districtId) {
        DistrictId = districtId;
    }

    public String getDetailAddress() {
        return DetailAddress;
    }

    public void setDetailAddress(String detailAddress) {
        DetailAddress = detailAddress;
    }

    public String getIsDefault() {
        return IsDefault;
    }

    public void setIsDefault(String isDefault) {
        IsDefault = isDefault;
    }
}
