package com.multshows.Beans;

import java.io.Serializable;

/**
 * 添加银行卡实体类
 */
public class UserBankAdd implements Serializable{
    private String UserId;//用户Id
    private String Bank;//所属银行简称
    private String Card;//卡号
    private int IsCredit;//是否信用卡：0,不是 1,是
    private String IdName;//真实姓名
    private String IdCard;//身份证号码
    private int CityId;//开户行地区
    private String BankName;//开户行名称
    private String ProName;//省份名
    private String CityName;//城市名
    private int IsDefault;//是否默认 0不默认 1默认

    public UserBankAdd() {
    }

    public String getProName() {
        return ProName;
    }

    public void setProName(String proName) {
        ProName = proName;
    }

    public String getCityName() {
        return CityName;
    }

    public void setCityName(String cityName) {
        CityName = cityName;
    }

    public int getIsDefault() {
        return IsDefault;
    }

    public void setIsDefault(int isDefault) {
        IsDefault = isDefault;
    }

    public String getUserId() {
        return UserId;
    }

    public void setUserId(String userId) {
        UserId = userId;
    }

    public String getBank() {
        return Bank;
    }

    public void setBank(String bank) {
        Bank = bank;
    }

    public String getCard() {
        return Card;
    }

    public void setCard(String card) {
        Card = card;
    }

    public int getIsCredit() {
        return IsCredit;
    }

    public void setIsCredit(int isCredit) {
        IsCredit = isCredit;
    }

    public String getIdName() {
        return IdName;
    }

    public void setIdName(String idName) {
        IdName = idName;
    }

    public String getIdCard() {
        return IdCard;
    }

    public void setIdCard(String idCard) {
        IdCard = idCard;
    }

    public int getCityId() {
        return CityId;
    }

    public void setCityId(int cityId) {
        CityId = cityId;
    }

    public String getBankName() {
        return BankName;
    }

    public void setBankName(String bankName) {
        BankName = bankName;
    }
}
