package com.multshows.Beans;

import java.io.Serializable;

/**
 * 交易单详情
 *
 */
public class OrderPayment implements Serializable{
    private String Id;//交易单Id
    private String UserId;//用户Id
    private String PaymentId;//订单id
    private double Amount;//支付金额
    private String OrderNumber;//订单编号
    private int PaidTime;//支付时间(时间戳)
    private String PaidTimeStr;//支付时间(字符串)
    private int PaymentType;//支付方式 1余额 2 一麻袋
    private String PaymentNumber;//付款编号
    private int Type;//交易类型 1订单交易 2打赏交易 3鲜花交易
    private int State;//支付状态 0 未支付 1已支付 2 取消支付
    private String PaymentTypeName;//支付方式名称
    private String StateName;//支付状态名称
    private UserHead User;//交易用户

    public OrderPayment() {
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getUserId() {
        return UserId;
    }

    public void setUserId(String userId) {
        UserId = userId;
    }

    public String getPaymentId() {
        return PaymentId;
    }

    public void setPaymentId(String paymentId) {
        PaymentId = paymentId;
    }

    public double getAmount() {
        return Amount;
    }

    public void setAmount(double amount) {
        Amount = amount;
    }

    public String getOrderNumber() {
        return OrderNumber;
    }

    public void setOrderNumber(String orderNumber) {
        OrderNumber = orderNumber;
    }

    public int getPaidTime() {
        return PaidTime;
    }

    public void setPaidTime(int paidTime) {
        PaidTime = paidTime;
    }

    public String getPaidTimeStr() {
        return PaidTimeStr;
    }

    public void setPaidTimeStr(String paidTimeStr) {
        PaidTimeStr = paidTimeStr;
    }

    public int getPaymentType() {
        return PaymentType;
    }

    public void setPaymentType(int paymentType) {
        PaymentType = paymentType;
    }

    public String getPaymentNumber() {
        return PaymentNumber;
    }

    public void setPaymentNumber(String paymentNumber) {
        PaymentNumber = paymentNumber;
    }

    public int getType() {
        return Type;
    }

    public void setType(int type) {
        Type = type;
    }

    public int getState() {
        return State;
    }

    public void setState(int state) {
        State = state;
    }

    public String getPaymentTypeName() {
        return PaymentTypeName;
    }

    public void setPaymentTypeName(String paymentTypeName) {
        PaymentTypeName = paymentTypeName;
    }

    public String getStateName() {
        return StateName;
    }

    public void setStateName(String stateName) {
        StateName = stateName;
    }

    public UserHead getUser() {
        return User;
    }

    public void setUser(UserHead user) {
        User = user;
    }
}
