package com.multshows.Beans;

import java.io.Serializable;

/**
 * 描述：动态详情图文描述bean
 * 作者：李萧萧
 * 时间：2016.10.9
 */
public class ActivityDetails_Beans implements Serializable{
    String mImage;//活动详情图片
    String mDescribe;//活动详情图片1=描述

    public ActivityDetails_Beans(String image, String describe) {
        mImage = image;
        mDescribe = describe;
    }

    public String getImage() {
        return mImage;
    }

    public void setImage(String image) {
        mImage = image;
    }

    public String getDescribe() {
        return mDescribe;
    }

    public void setDescribe(String describe) {
        mDescribe = describe;
    }
}
