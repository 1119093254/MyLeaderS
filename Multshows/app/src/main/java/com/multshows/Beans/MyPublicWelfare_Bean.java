public class MyPublicWelfare_Bean {
   List<MyPublicWelfare_Beans> mWelfareBeansList;
    String MybackgroundImage;//我的背景图片地址
    String myImage;//我的头像地址

    public MyPublicWelfare_Bean(List<MyPublicWelfare_Beans> welfareBeansList, String mybackgroundImage, String myImage) {
        mWelfareBeansList = welfareBeansList;
        MybackgroundImage = mybackgroundImage;
        this.myImage = myImage;
    }

    public List<MyPublicWelfare_Beans> getWelfareBeansList() {
        return mWelfareBeansList;
    }

    public void setWelfareBeansList(List<MyPublicWelfare_Beans> welfareBeansList) {
        mWelfareBeansList = welfareBeansList;
    }

    public String getMybackgroundImage() {
        return MybackgroundImage;
    }

    public void setMybackgroundImage(String mybackgroundImage) {
        MybackgroundImage = mybackgroundImage;
    }

    public String getMyImage() {
        return myImage;
    }

    public void setMyImage(String myImage) {
        this.myImage = myImage;
    }
}
