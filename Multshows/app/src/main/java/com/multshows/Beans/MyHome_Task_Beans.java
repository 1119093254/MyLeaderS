package com.multshows.Beans;

import java.util.List;

/**
 * 描述：任务界面bean
 * 作者：李萧萧
 * 时间：2016.9.27
 */
public class MyHome_Task_Beans {
    int year;//完成任务年月日
    int month;
    int day;
    String task;//任务名称
    double money;//奖励钱数
    List<String> mStringList;
    //没有年的话传０


    public MyHome_Task_Beans(int month, int day, int year, String task, double money, List<String> mStringList) {
        this.month = month;
        this.day = day;
        this.year = year;
        this.task = task;
        this.money = money;
        this.mStringList = mStringList;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }

    public double getMoney() {
        return money;
    }

    public List<String> getmStringList() {
        return mStringList;
    }

    public void setmStringList(List<String> mStringList) {
        this.mStringList = mStringList;
    }

    public void setMoney(double money) {
        this.money = money;
    }
}
