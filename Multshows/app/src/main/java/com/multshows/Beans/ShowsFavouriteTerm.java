package com.multshows.Beans;

import java.io.Serializable;

/**
 * 收藏列表 参数实体
 */
public class ShowsFavouriteTerm implements Serializable{
    private String UserId;//加密收藏者userId
    private String AuthorId;//加密作者id
    private String ShowsId;//加密作品id
    private int State;//状态-1全部0未收藏1已收藏
    private int BeginTime;//收藏开始时间
    private int EndTime;//收藏结束时间
    private int PageIndex;//页码
    private int PageSize;//每页显示条数

    public ShowsFavouriteTerm() {
    }

    public String getUserId() {
        return UserId;
    }

    public void setUserId(String userId) {
        UserId = userId;
    }

    public String getAuthorId() {
        return AuthorId;
    }

    public void setAuthorId(String authorId) {
        AuthorId = authorId;
    }

    public String getShowsId() {
        return ShowsId;
    }

    public void setShowsId(String showsId) {
        ShowsId = showsId;
    }

    public int getState() {
        return State;
    }

    public void setState(int state) {
        State = state;
    }

    public int getBeginTime() {
        return BeginTime;
    }

    public void setBeginTime(int beginTime) {
        BeginTime = beginTime;
    }

    public int getEndTime() {
        return EndTime;
    }

    public void setEndTime(int endTime) {
        EndTime = endTime;
    }

    public int getPageIndex() {
        return PageIndex;
    }

    public void setPageIndex(int pageIndex) {
        PageIndex = pageIndex;
    }

    public int getPageSize() {
        return PageSize;
    }

    public void setPageSize(int pageSize) {
        PageSize = pageSize;
    }
}
