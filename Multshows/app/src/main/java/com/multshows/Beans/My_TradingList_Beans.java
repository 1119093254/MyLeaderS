package com.multshows.Beans;

import java.io.Serializable;

/**
 * 交易记录对象
 */
public class My_TradingList_Beans implements Serializable{
    private int id;//记录id
    private String title;//记录名
    private String time;//记录时间
    private String money;//金额
    private boolean flag;//true 加;false 减

    public My_TradingList_Beans(int id, String title, String time, String money, boolean flag) {
        this.id = id;
        this.title = title;
        this.time = time;
        this.money = money;
        this.flag = flag;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public boolean isFlag() {
        return flag;
    }

    public void setFlag(boolean flag) {
        this.flag = flag;
    }
}
