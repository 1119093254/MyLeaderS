package com.multshows.Beans;

import java.io.Serializable;

/**
 * 添加打赏
 */
public class ShowsRewardAdd implements Serializable{
    String UserId;//加密收藏者userId
    String AuthorId;//加密作者id
    String ShowsId;//加密作品id
    double Amount;//打赏金额
    int State;//打赏状态 0未支付 1 已打赏 2 已撤回

    public String getUserId() {
        return UserId;
    }

    public void setUserId(String userId) {
        UserId = userId;
    }

    public String getAuthorId() {
        return AuthorId;
    }

    public void setAuthorId(String authorId) {
        AuthorId = authorId;
    }

    public String getShowsId() {
        return ShowsId;
    }

    public void setShowsId(String showsId) {
        ShowsId = showsId;
    }

    public double getAmount() {
        return Amount;
    }

    public void setAmount(double amount) {
        Amount = amount;
    }

    public int getState() {
        return State;
    }

    public void setState(int state) {
        State = state;
    }
}
