package com.multshows.Beans;

import java.io.Serializable;

/**
 * 作品分类实体
 */
public class ShowsBase implements Serializable{
    private String Id;//加密showsid
    private String Title;//标题
    private String SerialNumber;//作品编号
    private ShowsClassBase ShowsClass;//作品所属分类信息
    private String ShowsPhoto;//作品图片名称
    private String Infos;//内容
    private String VideoName;//视频名称
    private String LocationAddress;//地址


    public ShowsBase() {
    }

    public String getVideoName() {
        return VideoName;
    }

    public void setVideoName(String videoName) {
        VideoName = videoName;
    }

    public String getLocationAddress() {
        return LocationAddress;
    }

    public void setLocationAddress(String locationAddress) {
        LocationAddress = locationAddress;
    }

    public String getInfos() {
        return Infos;
    }

    public void setInfos(String infos) {
        Infos = infos;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public String getSerialNumber() {
        return SerialNumber;
    }

    public void setSerialNumber(String serialNumber) {
        SerialNumber = serialNumber;
    }

    public ShowsClassBase getShowsClass() {
        return ShowsClass;
    }

    public void setShowsClass(ShowsClassBase showsClass) {
        ShowsClass = showsClass;
    }

    public String getShowsPhoto() {
        return ShowsPhoto;
    }

    public void setShowsPhoto(String showsPhoto) {
        ShowsPhoto = showsPhoto;
    }
}
