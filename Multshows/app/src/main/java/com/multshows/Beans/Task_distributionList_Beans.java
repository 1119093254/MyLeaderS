package com.multshows.Beans;

import java.io.Serializable;

/**
 * 描述：任务分配宝宝列表
 * 作者：李萧萧
 * 时间：2016.10.8
 */
public class Task_distributionList_Beans implements Serializable{
    private String userId;//加密UserId
    private String username;//宝宝昵称
    private String phone;//宝宝手机号
    private String usetPic;//宝宝头像地址
    private int Birthday;//生日
    private int Sex;//性别 1男2女
    Boolean isChioce=false;//是否被选中

    public Task_distributionList_Beans(String userId, String username, String phone, String usetPic, int birthday, int sex, Boolean isChioce) {
        this.userId = userId;
        this.username = username;
        this.phone = phone;
        this.usetPic = usetPic;
        Birthday = birthday;
        Sex = sex;
        this.isChioce = isChioce;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public int getBirthday() {
        return Birthday;
    }

    public void setBirthday(int birthday) {
        Birthday = birthday;
    }

    public int getSex() {
        return Sex;
    }

    public void setSex(int sex) {
        Sex = sex;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUsetPic() {
        return usetPic;
    }

    public void setUsetPic(String usetPic) {
        this.usetPic = usetPic;
    }

    public Boolean getChioce() {
        return isChioce;
    }

    public void setChioce(Boolean chioce) {
        isChioce = chioce;
    }
}
