package com.multshows.Beans;

import java.io.Serializable;

/**
 * 消息 消息参数实体
 */
public class UserMessageTerm implements Serializable{
    private int Type;//0全部 1平台消息2提示信息3私聊消息4订单消息5任务消息6好友消息7互动消息
    private String SenderId;//加密发送者用户id
    private String ReceiverId;//加密接收者用户id
    private String Content;//聊天内容
    private int BeginTime;//记录时间的区间起点（时间戳）
    private int EndTime;//记录时间的区间终点（时间戳）
    private int PageIndex;//页码
    private int PageSize;//每页显示条数

    public UserMessageTerm() {
    }

    public int getType() {
        return Type;
    }

    public void setType(int type) {
        Type = type;
    }

    public String getSenderId() {
        return SenderId;
    }

    public void setSenderId(String senderId) {
        SenderId = senderId;
    }

    public String getReceiverId() {
        return ReceiverId;
    }

    public void setReceiverId(String receiverId) {
        ReceiverId = receiverId;
    }

    public String getContent() {
        return Content;
    }

    public void setContent(String content) {
        Content = content;
    }

    public int getBeginTime() {
        return BeginTime;
    }

    public void setBeginTime(int beginTime) {
        BeginTime = beginTime;
    }

    public int getEndTime() {
        return EndTime;
    }

    public void setEndTime(int endTime) {
        EndTime = endTime;
    }

    public int getPageIndex() {
        return PageIndex;
    }

    public void setPageIndex(int pageIndex) {
        PageIndex = pageIndex;
    }

    public int getPageSize() {
        return PageSize;
    }

    public void setPageSize(int pageSize) {
        PageSize = pageSize;
    }
}
