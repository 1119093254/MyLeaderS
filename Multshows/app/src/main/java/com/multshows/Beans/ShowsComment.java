package com.multshows.Beans;

import java.io.Serializable;

/**
 * 被评论  实体类
 */
public class ShowsComment implements Serializable{
    private String Id;//加密主键id
    private UserBase User;//用户信息
    private UserBase Author;//作者信息
    private UserBase TargetUser;//被回复评论者信息
    private ShowsBase Shows;//作品信息
    private Activity Activity;//活动信息
    private String ReplyId;//加密回复评论id
    private String Content;//评论内容
    private Task Task;//添加公益详情Task
    private int RecordTime;//记录时间
    private String RecordTimeStr;//记录时间对应字符

    public ShowsComment() {
    }

    public com.multshows.Beans.Task getTask() {
        return Task;
    }

    public void setTask(com.multshows.Beans.Task task) {
        Task = task;
    }

    public int getRecordTime() {
        return RecordTime;
    }

    public void setRecordTime(int recordTime) {
        RecordTime = recordTime;
    }

    public String getRecordTimeStr() {
        return RecordTimeStr;
    }

    public void setRecordTimeStr(String recordTimeStr) {
        RecordTimeStr = recordTimeStr;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public UserBase getUser() {
        return User;
    }

    public void setUser(UserBase user) {
        User = user;
    }

    public UserBase getAuthor() {
        return Author;
    }

    public void setAuthor(UserBase author) {
        Author = author;
    }

    public UserBase getTargetUser() {
        return TargetUser;
    }

    public void setTargetUser(UserBase targetUser) {
        TargetUser = targetUser;
    }

    public ShowsBase getShows() {
        return Shows;
    }

    public void setShows(ShowsBase shows) {
        Shows = shows;
    }

    public com.multshows.Beans.Activity getActivity() {
        return Activity;
    }

    public void setActivity(com.multshows.Beans.Activity activity) {
        Activity = activity;
    }

    public String getReplyId() {
        return ReplyId;
    }

    public void setReplyId(String replyId) {
        ReplyId = replyId;
    }

    public String getContent() {
        return Content;
    }

    public void setContent(String content) {
        Content = content;
    }
}
